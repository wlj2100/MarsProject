package mars.client;

public class Module {
	// module properties
    int code;
    String status;
    int turns;
    int xcoord;
    int ycoord;
    // constructor
    public Module(int code, String status, int turns, int xcoord, int ycoord) {
    	super();
    	this.code=code;
    	this.status=status;
    	this.turns=turns;
    	this.xcoord=xcoord;
    	this.ycoord=xcoord;
    }
}
